    private void jComboBoxPilihMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxPilihMenuActionPerformed
 
       jenis_menu=(String)jComboBoxPilihMenu.getSelectedItem();
                if(jenis_menu=="Ayam Goreng"){
                    harga=15000;
                }else if(jenis_menu=="Bebek Goreng"){
                    harga=20000;
                }
                else if(jenis_menu=="Es Teh"){
                    harga=3000;
                }else if(jenis_menu=="Es Jeruk"){
                    harga=5000;
                }  
        
        jTextFieldHarga.setText(""+harga);
    }//GEN-LAST:event_jComboBoxPilihMenuActionPerformed

