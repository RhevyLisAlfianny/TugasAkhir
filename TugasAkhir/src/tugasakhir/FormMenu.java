    private void jButtonHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHitungActionPerformed

        harga = Integer.parseInt(jTextFieldHarga.getText());
        jumlah = Integer.parseInt(jTextFieldJumlahPesan.getText());
        
        restoranProses totalByr = new Menu();
        total = totalByr.hitung(harga,jumlah);
        
        jTextFieldTotalHarga.setText(""+total);
    }//GEN-LAST:event_jButtonHitungActionPerformed

    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed

        Menu m = new Menu();
        m.setId(Integer.parseInt(jTextFieldId.getText()));
        m.setPilihanMenu((String) jComboBoxPilihMenu.getSelectedItem());
        m.setHarga(Integer.parseInt(jTextFieldHarga.getText()));
        m.setJumlahPesan(Integer.parseInt(jTextFieldJumlahPesan.getText()));
        m.setTotalHarga(Integer.parseInt(jTextFieldTotalHarga.getText()));
        m.save();
        tampilData();
    }//GEN-LAST:event_jButtonSimpanActionPerformed

    private void jTableMenuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMenuMouseClicked
        
        DefaultTableModel model = (DefaultTableModel)this.jTableMenu.getModel();
        int row = this.jTableMenu.getSelectedRow();
        Menu m = new Menu().getById(Integer.parseInt(model.getValueAt(row,0).toString()));
        this.jComboBoxPilihMenu.setSelectedItem(String.valueOf(m.getPilihanMenu()));

        this.jTextFieldHarga.setText(String.valueOf(m.getHarga()));
        this.jTextFieldJumlahPesan.setText(String.valueOf(m.getJumlahPesan()));

        this.jTextFieldTotalHarga.setText(String.valueOf(m.getTotalHarga()));

        
    }//GEN-LAST:event_jTableMenuMouseClicked

