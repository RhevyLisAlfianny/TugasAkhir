    public void tampilData(){
        String[] kolom = {"ID", "Pilihan Menu", "Harga", "Jumlah Pesan", "Total Harga"};
        ArrayList<Menu> list = new Menu().getAll();
        Object rowData[] = new Object[5];

        this.jTableMenu.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        for(int i = 0; i < list.size(); i++){
        rowData[0] = list.get(i).getId();
        rowData[1] = list.get(i).getPilihanMenu();
        rowData[2] = list.get(i).getHarga();
        rowData[3] = list.get(i).getJumlahPesan();
        rowData[4] = list.get(i).getTotalHarga();


       ((DefaultTableModel)this.jTableMenu.getModel()).addRow(rowData);
       }
    }
    
